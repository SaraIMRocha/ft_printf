//ascii 6 =  ACK - acknowledge

#include "ft_libftprintf.h"

void	ft_putstr(char *s, int *len)
{
	size_t	i;

	i = 0;
	if (!s)
	{
		write(1, "\0", 6);
		*len += 6;
	}
	else
		ft_putchar(s[i++], len);
}
